const navigationMenuTriggerStyle = cva(
  "group inline-flex h-9 w-max border border-transparent items-center justify-center rounded-xs bg-transparent px-4 py-2 text-sm font-medium text-white hover:bg-slate-800 hover:text-white focus:bg-slate-800 focus:text-white disabled:pointer-events-none disabled:opacity-50 data-[state=open]:hover:bg-slate-800 data-[state=open]:text-white data-[state=open]:focus:bg-slate-800 data-[state=open]:bg-slate-800/50 focus-visible:ring-slate-600/50 outline-none transition-all duration-100 focus-visible:ring-[3px] focus-visible:outline-1",
);

function NavigationMenuTrigger({
  className,
  children,
  ...props
}: React.ComponentProps<typeof NavigationMenuPrimitive.Trigger>) {
  return (
    <NavigationMenuPrimitive.Trigger
      data-slot="navigation-menu-trigger"
      className={cn(navigationMenuTriggerStyle(), "group", className)}
      {...props}
    >
      {children}{" "}
      <ChevronDownIcon
        className="relative top-[1px] ml-1 size-3 text-white transition duration-300 group-data-[state=open]:rotate-180"
        aria-hidden="true"
      />
    </NavigationMenuPrimitive.Trigger>
  );
}

